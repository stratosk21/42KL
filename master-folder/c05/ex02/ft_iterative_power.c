int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	original;

	i = 1;
	original = nb;
	if (nb == 0 && power == 0)
		return (1);
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	while (i < power)
	{
		original = nb * original;
		i++;
	}
	return (original);
}
